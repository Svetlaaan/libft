#include "libft.h"

void 	ft_bzero(void *s, size_t n)
{
	char	*ss;
	size_t	cou;

	ss = (char *)s;
	cou = 0;
	while (cou < n)
	{
		ss[cou] = '\0';
		cou++;
	}
}
